    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
 boolean ret=ValidateData();
      if(ret==true)
      {
        
        
        try
     {
         CallableStatement cs= StockManager.con.prepareCall("{call SavePurchase(?,?,?,?,?)}");
         cs.registerOutParameter(1,java.sql.Types.INTEGER);
         cs.setString(2,jTextField1.getText());
         cs.setInt(3,DistributorIDS.get(jComboBox1.getSelectedIndex()) );
         cs.setString(4,jTextField2.getText());
         cs.setString(5,jTextField3.getText());
         cs.execute();
         int PMID = cs.getInt(1);
         for(int i=0;i<jTable1.getRowCount();i++)
         {
             PreparedStatement ps= StockManager.con.prepareStatement("Insert into PurchaseDetails values(?,?,?,?,?) ");
             ps.setInt(1,Integer.parseInt(jTable1.getValueAt(i,0).toString()));
             ps.setInt(2,Integer.parseInt(jTable1.getValueAt(i,2).toString()));
             ps.setFloat(3,Float.parseFloat(jTable1.getValueAt(i,3).toString()));
             ps.setFloat(4,Float.parseFloat(jTable1.getValueAt(i,4).toString()));
             ps.setInt(5,PMID);
             ps.execute();
            
         }
          JOptionPane.showMessageDialog(rootPane,"Details Stored...");
           jTextField2.setText("");
          jTextField3.setText("");
     }
     catch(Exception ex)
     {
         JOptionPane.showConfirmDialog(rootPane, ex);
     }
     }
    }//GEN-LAST:event_jButton3ActionPerformed

